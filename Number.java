import java.util.Scanner;
class  Number1 {
    public static void main(String[] args) {
        int i,j,n;
        System.out.println("Input  number of n:");
        Scanner in = new
                Scanner(System.in);
        n= in.nextInt();
        for(i=1; i<=n; i++) {
            for (j = 1; j <= 1; j++)
                System.out.println(i);
            System.out.println("");
        }
    }
}